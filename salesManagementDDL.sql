create table product (p_id varchar2(4) primary key,p_name varchar2(20));
create table customer1 (c_id varchar2(4) primary key,c_name varchar2(20),c_phone int,c_address varchar2(30));
create table salesperson (sp_id varchar2(4) primary key,sp_name varchar2(30),sp_phone int);
create table marketingtype (mt_id varchar2(4) primary key,mt_name varchar2(30),vehiclereq varchar2(4));
create table salestarget (targetno int primary key,targetlevel varchar2(15),bonus float);
create table invoice (invoice_no int primary key,i_date date,i_amount float,sp_id varchar2(5),c_id varchar2(4),foreign key (sp_id) REFERENCES salesperson(sp_id),FOREIGN key (c_id) REFERENCES customer1 (c_id));
create table receipt (r_no int primary key,r_date date,billamount float,balance float,paymethod varchar2(20), invoice_no int,foreign key(invoice_no) references invoice (invoice_no));
create table orders (invoice_no int,foreign key(invoice_no) references invoice (invoice_no),p_id varchar2(4),foreign key(p_id) references product(p_id),product_unit int);
create table sp_marketingtype (mt_id varchar2(4),foreign key (mt_id) REFERENCES marketingtype(mt_id),sp_id varchar2(4), foreign key(sp_id) references salesperson (sp_id));
create table productcategory_target (targetno int,foreign key(targetno) references salestarget(targetno),p_category varchar2(4) ,produnits int);
alter table marketingtype add salary float;

